    report = report.replace('\\n', '\n').replace('\\r', '\r').replace('\\t', '\t')
    
    # –ó–∞–º–µ–Ω—è–µ–º —Ç–µ—Ö–Ω–∏—á–µ—Å–∫–∏–µ —Ç–µ—Ä–º–∏–Ω—ã –Ω–∞ –∫—Ä–∞—Å–∏–≤—ã–µ —Ä—É—Å—Å–∫–∏–µ –∞–Ω–∞–ª–æ–≥–∏
